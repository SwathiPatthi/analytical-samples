/* general guide for plus operator 
 Number + Number = Addition
 Boolean + Number = Addition
 Number + string = concatination
 string + boolean = concatination
 boolean + boolean = addition
*/

var bar=0; 
var foo = true;
console.log(bar + 10); //output: 10
console.log(bar + false);//output: 0
console.log(bar + 'swathi');//0swathi
console.log('swathi' + bar);//swathi0
console.log(false + foo);//1